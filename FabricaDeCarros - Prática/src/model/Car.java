package model;

public class Car {
    private String modelo;
    private String cor;

    public void setModel(String modelo) {
        this.modelo = modelo;
    }
    public void setColor(String cor) {
        this.cor = cor;
    }
    public String getModel() {
        return this.modelo;
    }
    public String getColor() {
        return this.cor;
    }
}
